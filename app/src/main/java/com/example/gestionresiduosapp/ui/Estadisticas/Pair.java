package com.example.gestionresiduosapp.ui.Estadisticas;

//Clase para representar un par de valores
public class Pair<F, S> {
    //Variables que representan el primer y segundo valor
    public F first;
    public S second;

    //Constructor
    public Pair(F first, S second) {
        this.first = first;
        this.second = second;
    }
}